export type Dil = "tr" | "en" | "de" | "fr" | "es";
export const DESTEKLENEN_DILLER: Dil[] = ["tr", "en", "de", "fr", "es"];
export const VARSAYILAN_DIL: Dil = "tr";

/** Dil meta (değiştirici + görüntü için). */
export const DIL_META: Record<Dil, { ad: string; yerelAd: string; bayrak: string }> = {
  tr: { ad: "Türkçe", yerelAd: "Türkçe", bayrak: "🇹🇷" },
  en: { ad: "İngilizce", yerelAd: "English", bayrak: "🇬🇧" },
  de: { ad: "Almanca", yerelAd: "Deutsch", bayrak: "🇩🇪" },
  fr: { ad: "Fransızca", yerelAd: "Français", bayrak: "🇫🇷" },
  es: { ad: "İspanyolca", yerelAd: "Español", bayrak: "🇪🇸" },
};

/** Bir değerin geçerli bir dil olup olmadığını doğrula (cookie/user girdisi için). */
export function gecerliDil(deger: unknown): deger is Dil {
  return typeof deger === "string" && (DESTEKLENEN_DILLER as string[]).includes(deger);
}

/** Herhangi bir girdiyi güvenli bir dile normalize et. */
export function dileCevir(deger: unknown): Dil {
  return gecerliDil(deger) ? deger : VARSAYILAN_DIL;
}

